package io_and_4ao;

import java.util.Scanner;

public class Divide {

  public static void main(String[] args) {
    
    Scanner sc = new Scanner(System.in);
    
    double a, b;
    a = sc.nextDouble();
    b = sc.nextDouble();
    
    System.out.println(a / b);
    
    sc.close();

  }

}
